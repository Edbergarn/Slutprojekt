public class Player {
    int id;
    String name;
    int HP;
    int weapon;
    int armor;
    int room;

    public Player(int id, String name, int HP, int weapon, int armor, int room) {
        this.id = id;
        this.name = name;
        this.HP = HP;
        this.weapon = weapon;
        this.armor = armor;
        this.room = room;

    }
}
